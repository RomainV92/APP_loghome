                            <li class="faq-answer">Un détecteur de fumée devrait être installé dans toutes les pièces habitables (salle de séjour, chambre à coucher, corridors). Dans les autres pièces, telles que la cuisine, il n'est pas adapté. Vous trouverez de plus amples informations dans la notice d'utilisation.</li>
                     

