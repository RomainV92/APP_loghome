                <h2 class='line'>Détecteur de mouvements</h2>
                    
                        <button class="question">Qu'est-ce que le détecteur de mouvement ? </button>
                           <li class="faq-answer">Le détecteur de mouvement  détecte et signale les accès non autorisés. </li>
                       

                        <button class="question">Comment le détecteur de mouvement peut-il distinguer les petits animaux ?   </button>
                           <li class="faq-answer">Tout être vivant émet de la chaleur. Les petits animaux émettent proportionnellement moins de chaleur que l'homme. Pour l'essentiel, la valeur seuil pour le capteur de chaleur (infrarouge) est relevée pour la détection de petits animaux.</li>
                     

                        <button class="question">Comment fonctionne la détection de mouvement ?</button>
                            <li class="faq-answer">Le détecteur de mouvement utilise une technologie infrarouge passive. Tous les corps (personnes, animaux, véhicules, arbres, etc.) émettent un rayonnement thermique infrarouge. Le détecteur de mouvement mesure et compare le rayonnement infrarouge dans sa plage de détection, n'émet cependant lui-même aucun rayonnement et, par conséquent, est passif.</li>
                        
                   
