                            <li class="faq-answer">Votre détecteur de fumée  commence à émettre un signal sonore puissant, à l'instar d'un détecteur de fumée classique. Tous les autres détecteurs de fumée  installés retentissent également.  Par ailleurs, vous recevez une notification et pouvez alerter les pompiers directement depuis le site. De plus, vous pouvez couper l'alarme des autres détecteurs de fumée activés depuis l'application. Pour des raisons de sécurité, vous ne pouvez pas désactiver à travers l'application le détecteur de fumée à l'origine du déclenchement.</li>
                        

